// examples of primitive datatypes

//number
var a;
a=30;
console.log(a);
console.log(typeof a);

console.log('----------------------------');

// string
var b="Javascript is awesome";
console.log(b);
console.log(typeof b);

console.log('-----------------------------');

// boolean
c=true;
console.log(c);
console.log(typeof c);

console.log('-----------------------------');

// undefined
d=undefined;
console.log(d, typeof d);

console.log('-----------------------------');

// null
// to represent an empty object in any language we use null
e=null;
console.log(e, typeof e);

console.log('-----------------------------');

// under number
console.log("Under 1.number;");

var p=10;
console.log(p); //integer

var q=10.111;
console.log(q); //float

var r=123.1234567891;
console.log(r); //double (decimal point >6digits)

// in js, it displays number as its type but in other languages it displays integer, float and all

var s=0x123;
console.log(s);  //hexadecimal
// s= 1*16^2 + 2*16^1 + 3*16^0

var l=0o123;
console.log(l);  //octal
// l= 1*8^2 + 2*8^1 + 3*8^0

console.log('-----------------------------');

// under boolean
console.log("Under 2.boolean;");

var x=true;
console.log(x);  //boolean

var y=false;
console.log(y);  //boolean


